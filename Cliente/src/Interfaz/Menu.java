    public void setProducto() {
        String[] datosServicio = SolicitarServicio.getDatosClienteServicio();
        ArrayList<String[]> datos = Conector.getProductos(datosServicio[0]);
        int numColumnas = datos.get(0).length + 1;
        String[] columnas = new String[numColumnas];
        for (int i = 0; i < numColumnas - 1; i++) {
            columnas[i] = datos.get(0)[i];
        }
        columnas[numColumnas - 1] = "Cantidad";
        Object[][] datosProductos = new Object[datos.size() - 1][numColumnas];
        for (int i = 1; i < datos.size(); i++) {
            for (int j = 0; j < datos.get(i).length; j++) {
                datosProductos[i - 1][j] = datos.get(i)[j];
            }
            datosProductos[i - 1][columnas.length - 1] = "0";
        }
        DefaultTableModel modeloTabla = new DefaultTableModel(datosProductos, columnas);
        jTblProducto.setModel(modeloTabla);
    }

